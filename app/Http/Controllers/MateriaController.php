     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'materia' => 'required',
            'disciplina' => 'required',
        ]);

        $materia = new Materia([
            'materia' => $request->get('materia'),
            'disciplina_id' => $request->get('disciplina'),
        ]);

        $materia->save();

